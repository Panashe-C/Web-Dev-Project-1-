for (int i = 1; i <= 100; i++)  
{  
        if (i % 3 == 0 && i % 5 == 0)  
        {  
            Console.WriteLine("FizzyBuzzy");  
        }  
        else if (i % 3 == 0)  
        {  
           Console.WriteLine("Fizzy");  
        }  
        else if (i % 5 == 0)  
        {  
           Console.WriteLine("Buzzy");  
        }  
        else  
        {  
            Console.WriteLine(i);  
        }  
} 

